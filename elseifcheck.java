class elseifcheck
{
	public static void main(String [] args)
	{
		int no=10;
		if(no>12)
		{
			System.out.println("Hello Vijay");
		}
		else if(no==10)
		{
			System.out.println("Hello1 Vijay");
		}
		else if(no<0)
		{
			System.out.println("Hello2 Vijay");
		}
		else
		{
			System.out.println("Hello");
		}
	}
}
